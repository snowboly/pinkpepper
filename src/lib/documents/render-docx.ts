// SOP renderer
// ============================================================

function renderSOP(data: SOPData): Paragraph[] {
  const elements: Paragraph[] = [
    ...versionBlock(data, data.title),
    heading("Purpose"),
    bodyText(data.purpose),
    heading("Scope"),
    bodyText(data.scope),
    heading("Responsibilities"),
    ...data.responsibilities.map((r) => bodyText(`• ${r}`)),
  ];

  if (data.equipment_materials?.length) {
    elements.push(heading("Equipment & Materials"));
    for (const item of data.equipment_materials) {
      elements.push(bodyText(`• ${item}`));
    }
  }

  elements.push(heading("Procedure"));
  for (const step of data.steps) {
    elements.push(
      new Paragraph({
        children: [
          new TextRun({ text: `${step.step_number}. ${step.action}`, bold: true, font: FONT, size: 22 }),
        ],
        spacing: { before: 120 },
      }),
      bodyText(step.details),
    );
    if (step.responsible) {
      elements.push(bodyText(`   Responsible: ${step.responsible}`));
    }
  }

  if (data.monitoring) {
    elements.push(heading("Monitoring"), bodyText(data.monitoring));
  }
  if (data.corrective_actions) {
    elements.push(heading("Corrective Actions"), bodyText(data.corrective_actions));
  }
  if (data.records?.length) {
    elements.push(heading("Records"));
    for (const rec of data.records) {
      elements.push(bodyText(`• ${rec}`));
    }
  }
  if (data.references?.length) {
    elements.push(heading("References"));
    for (const ref of data.references) {
      elements.push(bodyText(`• ${ref}`));
    }
  }

  return elements;
}


type SectionChild = Paragraph | Table;

function buildSectionChildren(doc: StructuredDocument): SectionChild[] {
  switch (doc.document_type) {
    case "haccp_plan": {
      const paragraphs = renderHACCPPlan(doc);
      const tables = buildHACCPTables(doc);
      // Insert tables after relevant headings
      const children: SectionChild[] = [];
      let haHeadingSeen = false;
      let ccpHeadingSeen = false;
      for (const p of paragraphs) {
        children.push(p);
        // Check if this paragraph contains "Hazard Analysis" heading
        if (!haHeadingSeen && p instanceof Paragraph) {
          const text = (p as unknown as { root?: unknown[] })?.constructor?.name;
          // Simple approach: insert table after hazard analysis heading
          const pText = JSON.stringify(p);
          if (pText.includes("Hazard Analysis")) {
            haHeadingSeen = true;
            if (tables[0]) children.push(tables[0]);
          }
          if (pText.includes("Critical Control Points")) {
            ccpHeadingSeen = true;
            if (tables[1]) children.push(tables[1]);
          }
        }
      }
      // Fallback: append tables at end if not inserted
      if (!haHeadingSeen && tables[0]) children.push(tables[0]);
      if (!ccpHeadingSeen && tables[1]) children.push(tables[1]);
      return children;
    }
    case "sop":
      return renderSOP(doc);
    case "cleaning_schedule":
      return [...renderCleaningSchedule(doc), buildCleaningTable(doc)];
    case "temperature_log":
      return [...renderTemperatureLog(doc), buildTemperatureTable(doc)];
    case "supplier_approval":
      return [...renderSupplierApproval(doc), buildSupplierTable(doc)];
  }
}

/**
 * Render a structured document into a DOCX buffer.
 */
export async function renderDocx(doc: StructuredDocument): Promise<Buffer> {
  const children = buildSectionChildren(doc);

  const document = new Document({
    sections: [
      {
        properties: {
          page: {
            margin: { top: 720, right: 720, bottom: 720, left: 720 },
          },
        },
        children,
      },
    ],
  });

  const buffer = await Packer.toBuffer(document);
  return Buffer.from(buffer);
}

// ============================================================
// Shared helpers
// ============================================================

const BRAND_COLOR = "E11D48";
const HEADER_BG = "FEF2F2";
const FONT = "Calibri";

function headerCell(text: string, width?: number): TableCell {
  return new TableCell({
    children: [
      new Paragraph({
        children: [new TextRun({ text, bold: true, font: FONT, size: 20, color: "0F172A" })],
        alignment: AlignmentType.LEFT,
      }),
    ],
    shading: { type: ShadingType.SOLID, color: HEADER_BG },
    width: width ? { size: width, type: WidthType.PERCENTAGE } : undefined,
  });
}

function cell(text: string, width?: number): TableCell {
  return new TableCell({
    children: [
      new Paragraph({
        children: [new TextRun({ text, font: FONT, size: 20 })],
      }),
    ],
    width: width ? { size: width, type: WidthType.PERCENTAGE } : undefined,
  });
}

function heading(text: string, level: (typeof HeadingLevel)[keyof typeof HeadingLevel] = HeadingLevel.HEADING_2): Paragraph {
  return new Paragraph({
    heading: level,
    children: [new TextRun({ text, font: FONT, color: "0F172A" })],
    spacing: { before: 240, after: 120 },
  });
}

function bodyText(text: string): Paragraph {
  return new Paragraph({
    children: [new TextRun({ text, font: FONT, size: 22 })],
    spacing: { after: 120 },
  });
}

function versionBlock(doc: { version: string; date: string; business_name: string; approved_by?: string }, title: string): Paragraph[] {
  return [
    new Paragraph({
      heading: HeadingLevel.TITLE,
      children: [new TextRun({ text: title, font: FONT, color: BRAND_COLOR })],
    }),
    new Paragraph({
      children: [
        new TextRun({ text: `Business: `, bold: true, font: FONT, size: 22 }),
        new TextRun({ text: doc.business_name, font: FONT, size: 22 }),
      ],
    }),
    new Paragraph({
      children: [
        new TextRun({ text: `Document No.: `, bold: true, font: FONT, size: 20 }),
        new TextRun({ text: `PP-${doc.date.replace(/-/g, "")}-001`, font: FONT, size: 20 }),
        new TextRun({ text: `   Version: `, bold: true, font: FONT, size: 20 }),
        new TextRun({ text: doc.version, font: FONT, size: 20 }),
        new TextRun({ text: `   Date: `, bold: true, font: FONT, size: 20 }),
        new TextRun({ text: doc.date, font: FONT, size: 20 }),
      ],
    }),
    new Paragraph({
      children: [
        new TextRun({ text: `Approved by: `, bold: true, font: FONT, size: 20 }),
        new TextRun({ text: doc.approved_by || "________________________", font: FONT, size: 20 }),
      ],
      spacing: { after: 200 },
    }),
    new Paragraph({
      children: [
        new TextRun({
          text: "AI-assisted draft generated by PinkPepper. Human validation required before operational use.",
          font: FONT,
          size: 18,
          italics: true,
          color: "64748B",
        }),
      ],
      spacing: { after: 300 },
      border: {
        bottom: { style: BorderStyle.SINGLE, size: 1, color: "E2E8F0" },
      },
    }),
  ];
}

// ============================================================
// HACCP Plan renderer
// ============================================================

function renderHACCPPlan(data: HACCPPlanData): Paragraph[] {
  const elements: Paragraph[] = [];

  elements.push(
    ...versionBlock(data, "HACCP Plan"),
    heading("1. Product Description"),
    bodyText(`Product: ${data.product_description}`),
    bodyText(`Intended use: ${data.intended_use}`),
  );

  if (data.target_consumer) {
    elements.push(bodyText(`Target consumer: ${data.target_consumer}`));
  }

  // Prerequisite programmes
  if (data.prerequisite_programmes?.length) {
    elements.push(heading("2. Prerequisite Programmes"));
    for (const prp of data.prerequisite_programmes) {
      elements.push(bodyText(`• ${prp}`));
    }
  }

  // Hazard analysis table
  elements.push(heading("3. Hazard Analysis"));

  const haRows = [
    new TableRow({
      tableHeader: true,
      children: [headerCell("Step"), headerCell("Hazard"), headerCell("Type"), headerCell("Control Measure"), headerCell("Monitoring")],
    }),
    ...data.process_steps.map(
      (s) =>
        new TableRow({
          children: [cell(s.step), cell(s.hazard), cell(s.hazard_type), cell(s.control_measure), cell(s.monitoring)],
        })
    ),
  ];

  elements.push(
    new Paragraph({ children: [] }), // spacer
  );

  // CCP table
  elements.push(heading("4. Critical Control Points"));

  const ccpRows = [
    new TableRow({
      tableHeader: true,
      children: [
        headerCell("CCP #"),
        headerCell("Name"),
        headerCell("Hazard"),
        headerCell("Critical Limit"),
        headerCell("Monitoring"),
        headerCell("Frequency"),
        headerCell("Corrective Action"),
        headerCell("Verification"),
        headerCell("Records"),
      ],
    }),
    ...data.ccps.map(
      (c) =>
        new TableRow({
          children: [
            cell(String(c.ccp_number)),
            cell(c.name),
            cell(c.hazard),
            cell(c.critical_limit),
            cell(c.monitoring_procedure),
            cell(c.monitoring_frequency),
            cell(c.corrective_action),
            cell(c.verification),
            cell(c.records),
          ],
        })
    ),
  ];

  if (data.review_frequency) {
    elements.push(heading("5. Review"));
    elements.push(bodyText(`Review frequency: ${data.review_frequency}`));
  }

  // We need to return paragraphs and tables interspersed. Since Document sections
  // accept (Paragraph | Table)[], we'll cast via a helper.
  // For now, embed tables as separate items by returning a flat array.
  return elements;
}


/**
 * Renders structured document data into professional PDF files using pdf-lib.
 */

import { PDFDocument, StandardFonts, rgb, PDFFont, PDFPage } from "pdf-lib";
import type {
  StructuredDocument,
  HACCPPlanData,
  SOPData,
  CleaningScheduleData,
  TemperatureLogData,
  SupplierApprovalData,
} from "./types";
import { DOCUMENT_TYPE_LABELS } from "./types";

const PAGE_WIDTH = 841.89; // A4 landscape for tables
const PAGE_HEIGHT = 595.28;
const MARGIN = 40;
const CONTENT_WIDTH = PAGE_WIDTH - 2 * MARGIN;

type RenderContext = {
  doc: PDFDocument;
  font: PDFFont;
  bold: PDFFont;
  page: PDFPage;
  y: number;
};

function newPage(ctx: RenderContext): void {
  ctx.page = ctx.doc.addPage([PAGE_WIDTH, PAGE_HEIGHT]);
  ctx.y = PAGE_HEIGHT - MARGIN;
}

function ensureSpace(ctx: RenderContext, needed: number): void {
  if (ctx.y - needed < MARGIN) {
    newPage(ctx);
  }
}

function drawTitle(ctx: RenderContext, text: string): void {
  ensureSpace(ctx, 30);
  ctx.page.drawText(text, { x: MARGIN, y: ctx.y, size: 18, font: ctx.bold, color: rgb(0.88, 0.11, 0.28) });
  ctx.y -= 28;
}

function drawHeading(ctx: RenderContext, text: string): void {
  ensureSpace(ctx, 24);
  ctx.y -= 8;
  ctx.page.drawText(text, { x: MARGIN, y: ctx.y, size: 13, font: ctx.bold, color: rgb(0.06, 0.09, 0.16) });
  ctx.y -= 20;
}

function drawText(ctx: RenderContext, text: string, x = MARGIN, size = 10): void {
  ensureSpace(ctx, 14);
  // Word wrap
  const maxWidth = PAGE_WIDTH - x - MARGIN;
  const words = text.split(/\s+/);
  let line = "";
  for (const word of words) {
    const test = line ? `${line} ${word}` : word;
    const width = ctx.font.widthOfTextAtSize(test, size);
    if (width > maxWidth && line) {
      ctx.page.drawText(line, { x, y: ctx.y, size, font: ctx.font, color: rgb(0.06, 0.09, 0.16) });
      ctx.y -= 14;
      ensureSpace(ctx, 14);
      line = word;
    } else {
      line = test;
    }
  }
  if (line) {
    ctx.page.drawText(line, { x, y: ctx.y, size, font: ctx.font, color: rgb(0.06, 0.09, 0.16) });
    ctx.y -= 14;
  }
}

function drawMeta(ctx: RenderContext, doc: { version: string; date: string; business_name: string; approved_by?: string }): void {
  drawText(ctx, `Business: ${doc.business_name}`);
  drawText(ctx, `Version: ${doc.version}  |  Date: ${doc.date}  |  Approved by: ${doc.approved_by || "________________________"}`);
  drawText(ctx, "AI-assisted draft generated by PinkPepper. Human validation required before operational use.");
  ctx.y -= 10;
  // Separator line
  ctx.page.drawLine({
    start: { x: MARGIN, y: ctx.y },
    end: { x: PAGE_WIDTH - MARGIN, y: ctx.y },
    thickness: 0.5,
    color: rgb(0.89, 0.91, 0.94),
  });
  ctx.y -= 16;
}

function drawTable(ctx: RenderContext, headers: string[], rows: string[][], colWidths?: number[]): void {
  const cols = headers.length;
  const defaultW = CONTENT_WIDTH / cols;
  const widths = colWidths ?? headers.map(() => defaultW);
  const rowHeight = 16;
  const cellPad = 4;
  const fontSize = 8;

  // Header
  ensureSpace(ctx, rowHeight + 4);
  let x = MARGIN;
  for (let i = 0; i < cols; i++) {
    // Header background
    ctx.page.drawRectangle({
      x,
      y: ctx.y - rowHeight + 4,
      width: widths[i],
      height: rowHeight,
      color: rgb(0.99, 0.95, 0.95),
    });
    const truncated = headers[i].substring(0, Math.floor(widths[i] / (fontSize * 0.5)));
    ctx.page.drawText(truncated, {
      x: x + cellPad,
      y: ctx.y - rowHeight + 8,
      size: fontSize,
      font: ctx.bold,
      color: rgb(0.06, 0.09, 0.16),
    });
    x += widths[i];
  }
  ctx.y -= rowHeight + 2;

  // Rows
  for (const row of rows) {
    ensureSpace(ctx, rowHeight + 2);
    x = MARGIN;
    for (let i = 0; i < cols; i++) {
      const cellText = (row[i] ?? "").substring(0, Math.floor(widths[i] / (fontSize * 0.45)));
      ctx.page.drawText(cellText, {
        x: x + cellPad,
        y: ctx.y - rowHeight + 8,
        size: fontSize,
        font: ctx.font,
        color: rgb(0.06, 0.09, 0.16),
      });
      x += widths[i];
    }
    // Row separator
    ctx.page.drawLine({
      start: { x: MARGIN, y: ctx.y - rowHeight + 2 },
      end: { x: PAGE_WIDTH - MARGIN, y: ctx.y - rowHeight + 2 },
      thickness: 0.3,
      color: rgb(0.89, 0.91, 0.94),
    });
    ctx.y -= rowHeight;
  }
  ctx.y -= 8;
}


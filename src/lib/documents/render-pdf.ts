function renderHACCP(ctx: RenderContext, data: HACCPPlanData): void {
  drawTitle(ctx, "HACCP Plan");
  drawMeta(ctx, data);

  drawHeading(ctx, "1. Product Description");
  drawText(ctx, `Product: ${data.product_description}`);
  drawText(ctx, `Intended use: ${data.intended_use}`);
  if (data.target_consumer) drawText(ctx, `Target consumer: ${data.target_consumer}`);

  if (data.prerequisite_programmes?.length) {
    drawHeading(ctx, "2. Prerequisite Programmes");
    for (const p of data.prerequisite_programmes) {
      drawText(ctx, `• ${p}`);
    }
  }

  drawHeading(ctx, "3. Hazard Analysis");
  drawTable(
    ctx,
    ["Step", "Hazard", "Type", "Control Measure", "Monitoring"],
    data.process_steps.map((s) => [s.step, s.hazard, s.hazard_type, s.control_measure, s.monitoring]),
  );

  drawHeading(ctx, "4. Critical Control Points");
  const ccpWidths = [30, 80, 80, 70, 90, 70, 100, 90, 90].map((w) => (w / 700) * CONTENT_WIDTH);
  drawTable(
    ctx,
    ["#", "CCP", "Hazard", "Limit", "Monitor", "Freq", "Corrective", "Verify", "Records"],
    data.ccps.map((c) => [
      String(c.ccp_number),
      c.name,
      c.hazard,
      c.critical_limit,
      c.monitoring_procedure,
      c.monitoring_frequency,
      c.corrective_action,
      c.verification,
      c.records,
    ]),
    ccpWidths,
  );

  if (data.review_frequency) {
    drawHeading(ctx, "5. Review");
    drawText(ctx, `Review frequency: ${data.review_frequency}`);
  }
}

function renderSOPPdf(ctx: RenderContext, data: SOPData): void {
  drawTitle(ctx, data.title);
  drawMeta(ctx, data);
  drawHeading(ctx, "Purpose");
  drawText(ctx, data.purpose);
  drawHeading(ctx, "Scope");
  drawText(ctx, data.scope);
  drawHeading(ctx, "Responsibilities");
  for (const r of data.responsibilities) drawText(ctx, `• ${r}`);
  drawHeading(ctx, "Procedure");
  for (const s of data.steps) {
    drawText(ctx, `${s.step_number}. ${s.action}`);
    drawText(ctx, `   ${s.details}`, MARGIN + 16);
  }
  if (data.monitoring) { drawHeading(ctx, "Monitoring"); drawText(ctx, data.monitoring); }
  if (data.corrective_actions) { drawHeading(ctx, "Corrective Actions"); drawText(ctx, data.corrective_actions); }
  if (data.references?.length) {
    drawHeading(ctx, "References");
    for (const ref of data.references) drawText(ctx, `• ${ref}`);
  }
}

function renderCleaningPdf(ctx: RenderContext, data: CleaningScheduleData): void {
  drawTitle(ctx, "Cleaning Schedule");
  drawMeta(ctx, data);
  drawTable(
    ctx,
    ["Area", "Task", "Frequency", "Method", "Chemical", "Conc.", "Time", "Responsible"],
    data.entries.map((e) => [e.area, e.task, e.frequency, e.method, e.chemical, e.concentration, e.contact_time, e.responsible]),
  );
}

function renderTempLogPdf(ctx: RenderContext, data: TemperatureLogData): void {
  drawTitle(ctx, "Temperature Monitoring Log");
  drawMeta(ctx, data);
  drawTable(
    ctx,
    ["Location", "Equipment", "Target", "Range", "Frequency", "Corrective Action"],
    data.columns.map((c) => [c.location, c.equipment, c.target_temp, c.acceptable_range, c.frequency, c.corrective_action]),
  );
}

function renderSupplierPdf(ctx: RenderContext, data: SupplierApprovalData): void {
  drawTitle(ctx, "Approved Supplier List");
  drawMeta(ctx, data);
  if (data.approval_criteria?.length) {
    drawHeading(ctx, "Approval Criteria");
    for (const c of data.approval_criteria) drawText(ctx, `• ${c}`);
  }
  drawTable(
    ctx,
    ["Supplier", "Products", "Status", "Last Audit", "Certificates", "Next Review"],
    data.suppliers.map((s) => [s.supplier_name, s.products_supplied, s.approval_status, s.last_audit_date ?? "—", s.certificates_held ?? "—", s.next_review_date ?? "—"]),
  );
}


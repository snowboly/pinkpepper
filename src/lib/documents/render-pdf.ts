/**
 * Render a structured document into a PDF buffer.
 */
export async function renderPdf(data: StructuredDocument): Promise<Uint8Array> {
  const pdfDoc = await PDFDocument.create();
  const font = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const bold = await pdfDoc.embedFont(StandardFonts.HelveticaBold);
  const page = pdfDoc.addPage([PAGE_WIDTH, PAGE_HEIGHT]);

  const ctx: RenderContext = { doc: pdfDoc, font, bold, page, y: PAGE_HEIGHT - MARGIN };

  switch (data.document_type) {
    case "haccp_plan":
      renderHACCP(ctx, data);
      break;
    case "sop":
      renderSOPPdf(ctx, data);
      break;
    case "cleaning_schedule":
      renderCleaningPdf(ctx, data);
      break;
    case "temperature_log":
      renderTempLogPdf(ctx, data);
      break;
    case "supplier_approval":
      renderSupplierPdf(ctx, data);
      break;
  }

  return pdfDoc.save();
}
